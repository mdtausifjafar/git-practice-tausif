# Define a function 'add' that takes two arguments, 'a' and 'b'
# It returns the sum of 'a' and 'b'
def add(a, b):
    return a + b

# Define a function 'subtract' that takes two arguments, 'a' and 'b'
# It returns the difference of 'a' and 'b'
def subtract(a, b):
    return a - b

# Define a function 'multiply' that takes two arguments, 'a' and 'b'
# It returns the product of 'a' and 'b'
def multiply(a, b):
    return a * b